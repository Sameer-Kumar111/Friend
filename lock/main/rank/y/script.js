// Bold Button
document.getElementById('boldBtn').onclick = function() {
    document.getElementById('text').style.fontWeight = 'bold';
};

// Italic Button
document.getElementById('italicBtn').onclick = function() {
    document.getElementById('text').style.fontStyle = 'italic';
};

// Underline Button
document.getElementById('underlineBtn').onclick = function() {
    document.getElementById('text').style.textDecoration = 'underline';
};

// Uppercase Button
document.getElementById('uppercaseBtn').onclick = function() {
    document.getElementById('text').style.textTransform = 'uppercase';
};



